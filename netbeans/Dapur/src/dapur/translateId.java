public class translateId {

    /**
     * @param args the command line arguments
    */
    String idMak;
    int porsi;
    
    public translateId(String idMak,int porsi){
        this.idMak = idMak;
        this.porsi = porsi;
    }
    
    public String getIdMak(){
        return idMak;
    }
    public int getPorsi(){
        return porsi;
    }
    
    
}
